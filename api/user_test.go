type eqCostumedMatcher struct {
	arg      db.CreateUserParams
	password string
}

func (e eqCostumedMatcher) Matches(x interface{}) bool {
	// In case, some value is nil
	if x == nil {
		return false
	}
	fmt.Println("It's coming from request", x)
	fmt.Println("It's coming from origin", e.arg)
	arg, ok := x.(db.CreateUserParams)
	if !ok {
		return false
	}
	err := util.CheckPassword(e.password, arg.HashedPassword)
	if err != nil {
		return false
	}
	e.arg.HashedPassword = arg.HashedPassword
	return reflect.DeepEqual(e.arg, arg)
}
func (e eqCostumedMatcher) String() string {
	return fmt.Sprintln(" password is equal")
}

func CostumedEq(arg db.CreateUserParams, password string) gomock.Matcher {
	return eqCostumedMatcher{arg, password}
}
