func TestGetUser(t *testing.T) {
	user, _ := randomUser(t)
	testCases := []struct {
		name          string
		userName      string
		buildStubs    func(store *mockdb.MockStore)
		checkResponse func(recoder *httptest.ResponseRecorder)
	}{
		{
			name:     "OK",
			userName: user.Username,
			buildStubs: func(store *mockdb.MockStore) {
				store.EXPECT().GetUser(gomock.Any(), user.Username).Times(1).Return(user, nil)
			},
			checkResponse: func(recorder *httptest.ResponseRecorder) {
				require.Equal(t, http.StatusOK, recorder.Code)
				requireBodyMatchUser(t, recorder.Body, user)
			},
		},
		{
			name:     "Invalid",
			userName: "vjfkjvh##ff",
			buildStubs: func(store *mockdb.MockStore) {
				store.EXPECT().GetUser(gomock.Any(), gomock.Any()).Times(0)
			},
			checkResponse: func(recorder *httptest.ResponseRecorder) {
				require.Equal(t, http.StatusBadRequest, recorder.Code)
				CheckErrGetUser(t, recorder)
			},
		},
		{
			name:     "NotFound",
			userName: "cantfindme",
			buildStubs: func(store *mockdb.MockStore) {
				store.EXPECT().GetUser(gomock.Any(), gomock.Eq("cantfindme")).Times(1).Return(db.User{}, sql.ErrNoRows)
			},
			checkResponse: func(recorder *httptest.ResponseRecorder) {
				require.Equal(t, http.StatusNotFound, recorder.Code)
				CheckErrGetUser(t, recorder)
			},
		},
		{
			name:     "InternalError",
			userName: user.Username,
			buildStubs: func(store *mockdb.MockStore) {
				store.EXPECT().GetUser(gomock.Any(), gomock.Any()).Times(1).Return(db.User{}, sql.ErrTxDone)
			},
			checkResponse: func(recorder *httptest.ResponseRecorder) {
				require.Equal(t, http.StatusInternalServerError, recorder.Code)
				CheckErrGetUser(t, recorder)
			},
		},
	}
	for _, v := range testCases {
		t.Run(v.name, func(t *testing.T) {
			ctrl := gomock.NewController(t)
			defer ctrl.Finish()

			store := mockdb.NewMockStore(ctrl)
			v.buildStubs(store)

			server := newTestServer(t, store)
			recorder := httptest.NewRecorder()
			_url := fmt.Sprintf("/user/%s", v.userName)
			encodedURL := url.QueryEscape(_url)
			request, err := http.NewRequest(http.MethodGet, encodedURL, nil)
			require.NoError(t, err)
			token, payload, err := server.tokenMaker.CreateToken(v.userName, "", time.Minute)
			require.NoError(t, err)
			require.NotEmpty(t, payload)
			authorizationHeader := fmt.Sprintf("%s %s", authorizationHeaderTypes[0], token)
			request.Header.Set(authorizationHeaderKey, authorizationHeader)
			server.router.ServeHTTP(recorder, request)
			v.checkResponse(recorder)
		})
	}
}

func requireBodyMatchUser(t *testing.T, body *bytes.Buffer, user db.User) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var gotUser db.User
	err = json.Unmarshal(data, &gotUser)
	require.NoError(t, err)
	require.Equal(t, user.Username, gotUser.Username)
	require.Equal(t, user.FullName, gotUser.FullName)
	require.Equal(t, user.Email, gotUser.Email)
	require.NotEmpty(t, gotUser.HashedPassword)
}

func CheckErr(t *testing.T, recorder *httptest.ResponseRecorder) {
	var res map[string]interface{}
	err := json.Unmarshal(recorder.Body.Bytes(), &res)
	require.NoError(t, err)
	_, exists := res["error"]
	require.Equal(t, true, exists)
	require.Equal(t, http.StatusBadRequest, recorder.Code)
}

